package com.studenttrack.dto;

import com.studenttrack.entity.LiveClass;
import java.time.LocalDateTime;

public class LiveClassDTO {
    private Long id;
    private String title;
    private String description;
    private String subject;
    private Long classId;
    private LocalDateTime scheduledAt;
    private Integer durationMinutes;
    private LiveClass.Platform platform;
    private String meetingUrl;
    private String meetingId;
    private String meetingPassword;
    private LiveClass.Status status;
    private String recordingUrl;

    // Constructors
    public LiveClassDTO() {}

    // Getters and Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getSubject() {
        return subject;
    }

    public void setSubject(String subject) {
        this.subject = subject;
    }

    public Long getClassId() {
        return classId;
    }

    public void setClassId(Long classId) {
        this.classId = classId;
    }

    public LocalDateTime getScheduledAt() {
        return scheduledAt;
    }

    public void setScheduledAt(LocalDateTime scheduledAt) {
        this.scheduledAt = scheduledAt;
    }

    public Integer getDurationMinutes() {
        return durationMinutes;
    }

    public void setDurationMinutes(Integer durationMinutes) {
        this.durationMinutes = durationMinutes;
    }

    public LiveClass.Platform getPlatform() {
        return platform;
    }

    public void setPlatform(LiveClass.Platform platform) {
        this.platform = platform;
    }

    public String getMeetingUrl() {
        return meetingUrl;
    }

    public void setMeetingUrl(String meetingUrl) {
        this.meetingUrl = meetingUrl;
    }

    public String getMeetingId() {
        return meetingId;
    }

    public void setMeetingId(String meetingId) {
        this.meetingId = meetingId;
    }

    public String getMeetingPassword() {
        return meetingPassword;
    }

    public void setMeetingPassword(String meetingPassword) {
        this.meetingPassword = meetingPassword;
    }

    public LiveClass.Status getStatus() {
        return status;
    }

    public void setStatus(LiveClass.Status status) {
        this.status = status;
    }

    public String getRecordingUrl() {
        return recordingUrl;
    }

    public void setRecordingUrl(String recordingUrl) {
        this.recordingUrl = recordingUrl;
    }
}
